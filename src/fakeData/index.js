import arts from './arts';
import commerce from './commerce';
import it from './it';

const fakeData = [...arts, ...commerce, ...it];


const shuffle = a => {
    for (let i = a.length; i; i--) {
        let j = Math.floor(Math.random() * i);
        [a[i - 1], a[j]] = [a[j], a[i - 1]];
    }
}

shuffle(fakeData);

export default fakeData;